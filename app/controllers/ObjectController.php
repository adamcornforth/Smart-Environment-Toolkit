	/**
	 * Update the specified resource in storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function update($id)
	{
		$object = Object::find($id); 

		/**
		 * Update object title if title supplied
		 */
		if(Input::has('title')) {
			$object->title = Input::get('title');
			$object->save(); 
		}

		return Redirect::to('objects/'.$object->id);
	}


